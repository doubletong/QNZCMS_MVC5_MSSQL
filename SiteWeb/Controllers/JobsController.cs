using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Threading.Tasks;
using System.Net;
using System.Web;
using System.Web.Mvc;
using TZGCMS.Data.Entity;
using TZGCMS.Model.Front.ViewModel.Jobs;
using TZGCMS.Data.Enums;

namespace TZGCMS.SiteWeb.Controllers
{
    public class JobsController : Controller
    {
        private TZGEntities db = new TZGEntities();

        // GET: Jobs
        [Route("jobs")]
        [Route("jobs/{seoName}", Name = "jobDetail")]
        public async Task<ActionResult> Index(string seoName)
        {
            var job = string.IsNullOrEmpty(seoName) ? await db.Jobs.FirstOrDefaultAsync() : await db.Jobs.FirstOrDefaultAsync(d => d.SeoName == seoName);
            PageJobVM vm = new PageJobVM
            {
                Jobs = await db.Jobs.Where(d => d.Active).OrderByDescending(d => d.Importance).ToListAsync(),
                Job = job,
                SeoName = job?.SeoName
            };
            var url = Request.RawUrl;
            ViewBag.PageMeta = await db.PageMetas.FirstOrDefaultAsync(d=>d.ModelType == ModelType.MENU && d.ObjectId==url );

            return View(vm);
        }

        // GET: Jobs/Details/5
        public async Task<ActionResult> Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Job job = await db.Jobs.FindAsync(id);
            if (job == null)
            {
                return HttpNotFound();
            }
            return View(job);
        }

        // GET: Jobs/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Jobs/Create
        // 为了防止“过多发布”攻击，请启用要绑定到的特定属性，有关 
        // 详细信息，请参阅 https://go.microsoft.com/fwlink/?LinkId=317598。
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Create([Bind(Include = "Id,Post,SeoName,Description,Importance,Active,Category,Address,Quantity,CreatedDate,CreatedBy,UpdatedDate,UpdatedBy")] Job job)
        {
            if (ModelState.IsValid)
            {
                db.Jobs.Add(job);
                await db.SaveChangesAsync();
                return RedirectToAction("Index");
            }

            return View(job);
        }

        // GET: Jobs/Edit/5
        public async Task<ActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Job job = await db.Jobs.FindAsync(id);
            if (job == null)
            {
                return HttpNotFound();
            }
            return View(job);
        }

        // POST: Jobs/Edit/5
        // 为了防止“过多发布”攻击，请启用要绑定到的特定属性，有关 
        // 详细信息，请参阅 https://go.microsoft.com/fwlink/?LinkId=317598。
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Edit([Bind(Include = "Id,Post,SeoName,Description,Importance,Active,Category,Address,Quantity,CreatedDate,CreatedBy,UpdatedDate,UpdatedBy")] Job job)
        {
            if (ModelState.IsValid)
            {
                db.Entry(job).State = EntityState.Modified;
                await db.SaveChangesAsync();
                return RedirectToAction("Index");
            }
            return View(job);
        }

        // GET: Jobs/Delete/5
        public async Task<ActionResult> Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Job job = await db.Jobs.FindAsync(id);
            if (job == null)
            {
                return HttpNotFound();
            }
            return View(job);
        }

        // POST: Jobs/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> DeleteConfirmed(int id)
        {
            Job job = await db.Jobs.FindAsync(id);
            db.Jobs.Remove(job);
            await db.SaveChangesAsync();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
